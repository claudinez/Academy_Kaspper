/*
Escreva um algoritmo para imprimir os 10 primeiros números inteiros maiores que 100. 
*/

// Inicializa a variável para começar a contar a partir de 101
var numero = 101;

// Utiliza um loop for para imprimir os 10 primeiros números inteiros maiores que 100
for (var i = 0; i < 10; i++) {
    console.log(numero);
    numero++;
}

