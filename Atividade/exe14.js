/*
14)	Ler um valor e escrever se é positivo ou negativo (considere o valor zero como positivo). 
*/

// Valor de exemplo
var valor = -6;  

// Verifica se o valor é positivo ou negativo
if (valor >= 0) {
    console.log("O valor é positivo.");
} else {
    console.log("O valor é negativo.");
}
